/*********************************************************************
 			字符串长度
**********************************************************************/
int strlen(const char *str)
{
	int i = 0;
	while(*str != '\0')
	{	
		i++;
		str++;
	}
	return i;
}

/************************************************************************
 			字符位置查找
*************************************************************************/
int strchr(char *str, char ch)
{
	int i = 0;
	while(*str && *str != ch)
	{
		i++;
	}
	i = (*str == ch)? i:0;
	return i;
}

/***************************************************************************
 			字符串比较
***************************************************************************/
int strcmp(char *str1, char *str2)
{
	char *p = str1;
	char *q = str2;
	do{
		if((*p)++ > (*q)++)
			return 1;
		else if((*p)++ < (*q)++)
			return -1;
		else{
			p++;
			q++;
		}
	}while(*p && *q);
	
	if(*p != 0)
		return 1;
	else if(*q != 0)
		return -1;
	return 0;	
}

/********************************************************************************
 				字符串追加
 ********************************************************************************/
void strcat(char *src, char *dest)
{
	char *p = src;
	char *q = dest;
	/* 指针指向字符串结尾 */
	while(*p)
		p++;
	while(*q)
	{
		*p = *q;
		q++;
		p++;
	}
	*p = '\0';
}
